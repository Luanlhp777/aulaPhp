<?php
// PODEMOS INCREMENTAR UM VALOR OU DECREMENTAR COM OS OPERADORES ++ E --
// EXEMPLO $N++ OU $X--
// ONDE N E X SÃO VARIÁVEIS, E TERÃO SEUS VALORES ALTERADOS COM +1 E -1
// ESTES OPERADORES SÃO MUITO UTILIZADOS EM ESTRUTURAS DE REPETIÇÃO

    $x = 0;
    $y = 0;

    $x++; //INCREMENTO
    $y = $y + 1;

    echo "x: " . $x . "<br>";
    echo "y: " . $y . "<br>";

    $n = 10;
    $m = 10;

    $n--; //DECREMENTO
    $m = $m - 1;

    echo "n: " . $n . "<br>";
    echo "m: " . $m . "<br>";